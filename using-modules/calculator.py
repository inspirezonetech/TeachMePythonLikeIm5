# Creating a module with functions inside, in this case
# A calculator module to sum and multiply

def sum_two_numbers(a, b):
    result = a + b
    return f'result of sum {a} + {b} = {result}'


def multiply_two_numbers(a, b):
    result = a * b
    return f'result of multiply {a} X {b} = {result}'

